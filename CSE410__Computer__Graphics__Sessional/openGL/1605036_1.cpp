	u.setValues(0, 0, 1);
	double temp = 1.0/sqrt(2.0);
	r.setValues(temp * -1.0, temp, 0);
	l.setValues(temp * -1.0, temp * -1.0, 0);

	//clear the screen
	glClearColor(0,0,0,0);

	/************************
	/ set-up projection here
	************************/
	//load the PROJECTION matrix
	glMatrixMode(GL_PROJECTION);

	//initialize the matrix
